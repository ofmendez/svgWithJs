const mapInteractive = (countries) => {
	const events = ['mouseover', 'mouseout', 'click'];
	const colors = ['white', '#34365F', 'yellow'];

	for (let country of countries) {
		country = document.getElementById(country);

		if (!country)
			continue;

		for (let i = 0; i < events.length; i++){
			country.addEventListener(events[i], () => {
				if (country.children.length > 0)
					for (const land of country.children){
						land.style.fill = colors[i];
					}
				else
					country.style.fill = colors[i];
					console.log(country.id);
			});
		}
	}
}

